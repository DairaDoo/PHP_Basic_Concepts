echo $x - $y . "<br>"; // subtraction
echo $x * $y . "<br>"; // multiplication
echo $y / $x . "<br>"; // division
echo $y % $x . "<br>"; // modulus
echo $x ** 2 . "<br>"; // exponentiation

echo min($x, $y) . "<br>"; // minimum value
echo max($x, $y) . "<br>"; // maximum value

